import fs from "node:fs";
import net from "node:net";
import path from "node:path";
import { fileURLToPath } from "node:url";

const API_PREFERRED_PORT = 5050;
const WEB_PREFERRED_PORT = 5173;
const HOST = "127.0.0.1";
const LOCALHOST = "localhost";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");

function parsePort(value, fallback) {
  const port = Number.parseInt(String(value || ""), 10);
  return Number.isInteger(port) && port > 0 && port < 65536 ? port : fallback;
}

function canListen(port, host) {
  return new Promise((resolve, reject) => {
    const server = net.createServer();

    server.once("error", (error) => {
      if (error?.code === "EADDRINUSE") return resolve(false);
      return reject(error);
    });
    server.once("listening", () => {
      server.close(() => resolve(true));
    });

    server.listen(port, host);
  });
}

async function nextAvailablePort(preferredPort, reserved = new Set()) {
  for (let port = preferredPort; port < 65536; port += 1) {
    if (reserved.has(port)) continue;
    if (await canListen(port, HOST)) return port;
  }

  throw new Error(`No available port found at or above ${preferredPort}`);
}

function writeEnvFile(filePath, values, header) {
  const lines = [
    "# Generated by npm run dev:prepare.",
    "# Local-only file; do not commit.",
    `# ${header}`,
    "",
    ...Object.entries(values).map(([key, value]) => `${key}=${value}`),
    "",
  ];

  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, lines.join("\n"), "utf8");
}

const apiPreferredPort = parsePort(process.env.PARAMETRICS_API_PORT, API_PREFERRED_PORT);
const webPreferredPort = parsePort(process.env.PARAMETRICS_WEB_PORT, WEB_PREFERRED_PORT);

const reserved = new Set();
const apiPort = await nextAvailablePort(apiPreferredPort, reserved);
reserved.add(apiPort);
const webPort = await nextAvailablePort(webPreferredPort, reserved);

const apiBaseUrl = `http://${HOST}:${apiPort}`;
const webOrigin = `http://${HOST}:${webPort}`;
const webLocalhostOrigin = `http://${LOCALHOST}:${webPort}`;

writeEnvFile(
  path.join(repoRoot, "apps/api/.env.local"),
  {
    PORT: apiPort,
    APP_PUBLIC_API_BASE: apiBaseUrl,
    APP_URL: webOrigin,
    CORS_ORIGINS: `${webOrigin},${webLocalhostOrigin}`,
  },
  "Resolved API and browser origins for local API startup.",
);

writeEnvFile(
  path.join(repoRoot, "apps/web/.env.local"),
  {
    VITE_API_PORT: apiPort,
    VITE_WEB_PORT: webPort,
    VITE_API_BASE_URL: apiBaseUrl,
    VITE_WEB_ORIGIN: webOrigin,
  },
  "Resolved API and web ports for local Vite startup.",
);

console.log("ParaMetrics local dev ports prepared");
console.log(`API_URL=${apiBaseUrl}`);
console.log(`WEB_URL=${webOrigin}`);
console.log(`VITE_API_BASE_URL=${apiBaseUrl}`);
console.log(`CORS_ORIGINS=${webOrigin},${webLocalhostOrigin}`);
console.log("Generated: apps/api/.env.local");
console.log("Generated: apps/web/.env.local");
